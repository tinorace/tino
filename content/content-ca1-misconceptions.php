<h2>Misconceptions in Science: </h2>
<h3>About the project</h3>
<p>This was Curriculum Assignment 1 for the Oxford University PGCE course.</p>

<h3>What I did</h3>
<p>I started by researching a suitable area in which KS3 students have misconceptions in Science. I then resaerched what sort of misconception they might have and talked to other teachers about this area. I interviewed six students in Year 7 with a range of abilities and questioned them about this topic to identify what misconceptions they had.</p>

<h3>Summary of findings</h3>



<h3>Outcome</h3>
<p>As well as through my own research, I was able to learn about a range of misconceptions that students have in Science at all age levels. This has helped me deal with these misconceptions early on in the teaching of each topic. I can recommend the use of Concept Cartoons at the start of a lesson or topic to help identify what misconceptions your class has and who in the class has them.</p>

