<h2>Innovations in ICT</h2>
<h3>About the project</h3>
ICT is meant to be used as an interactive tool in teaching, yet the ironic thing is that it was made teaching less interactive as it has become an alternative to chalk an talk: the dreaded 'death by PowerPoint'. I attended a course at the National Science Learning Centre (NSLC), York, to address these issues. Three days in February, followed by a research task in school, and finally three days back at the NSLC resulted in some really positive outcome for the Science department at Lord Williams's School
<h3>What I do</h3>
Aside from learning too many ICT tools to mention here which would take many years to fully investigate in my own classroom, there were a few main things I did.
<ul><li>Podasting</li><li>Creating websites</li><li>Using wikis</li><li>Alternatives to PowerPoint</li></ul>

