<h2>Part II Mapping Project: Villamanin, Spain</h2>
<h3>About the project</h3>
<p>This mapping project was a requirement of the Geology course during my third year. I mapped around the area to the south and east of <a href="http://en.wikipedia.org/wiki/Villamanin" target="blank">Villamanin, Spain</a> with my mapping partner <a href="http://www1.gly.bris.ac.uk/~JamesVerdon/index.html" target="blank">James Verdon</a>, while the adjacent area was mapped by <a href="http://homepages.see.leeds.ac.uk/~lechlm/" target="blank">Helen Macintyre</a> and <a href="http://uk.linkedin.com/pub/aileen-dennis/10/171/705" target="blank">Aileen Dennis</a>. It was carried out between 13th July - 19th August 2004.</p>

<h3>What I did</h3>
<p>I spent 29 days mapping the area of Villamanin to report on the geological history of the Southern Cantabrian Mountains in North-west Spain. During this time I mapped 20km<sup>2</sup>.</p>

<h3>Summary of geological history</h3>
<ul>
<h4><strong>Cambro-Ordovician</h4>
	<li>Lancara formation:</strong> Calcium carbonte accumulated on a shallow marine shelf, and its basal layer is dolomitised.</li>
<li><strong>Griotte formation:</strong> Sea-level drop allows terrigenous input to become significant, indicated by ferric content of rocks.</li>
<li><strong>Orville formation:</strong> Further drop in sea-level causes sandy conditions, indicated by tidal ripple marks and bioturbation.</li>
<li><strong>Barrios formation:</strong> Area become deltaic after further fall in sea-level, indicated by cross-bedding from migrating dunes and challow water, and infilling of migrating channels with shale. The dunes are diagentically altered to orthoquartize during a hiatus</li>
<h4>Silurian</h4>
<li><strong>Formigoso formation:</strong> After a large sea-level rise during the hiatus, thinly laminated shale is deposited in deep water. Bioturbation causes reducing conditions, forming glauconite.</li>
<li><strong>San Pedro formation:</strong> A quick fall in sea-level increases the iron input, giving way to sandstones</li>
<h4>Devonian</h4>
<li><strong>La Vid formation:</strong> The sea level rises but is contantly fluctuating, indicated by interbedded limestones and shales. Conditions are quiet as indicated by a well preserved and diverse fauna. Basal limestones are dolomitised.</li>
<li><strong>Villasimpliz formation:</strong> As the sea-level continues to rise, non-calcareous shales form and the water is too deep for an significant organic input into the rock.</li>
</ul>


