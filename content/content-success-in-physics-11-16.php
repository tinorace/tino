<h2>Success in Physics 11-16</h2>
<h3>About the project</h3>
<p>This two part course was for teachers who are experienced and confident in teaching secondary physics. I found out how my pupils could learn physics by engaging with research and applying it to my classroom practice. I tried a wide range of activities which I then used back in the classroom.</p>

<h3>What I did</h3>
<p>I did a gap task before the second residential period, entitled <strong>"Rocks... what's the point?: Improving engagement through enquiry"</strong>. I chose this as while it was not a Physics module, I was confident with content as a Geology specialist, and there were limited opportunities in teaching Physics in the time I had to conduct the research. The aims of the study were to:</p>

<ul>
<li>Investigate ways of increasing engagement in our Year 8 module called "Rocks".</li>
<li>Use a cross-curricular approach (within science disciplines)</li>
<li>Use rich questions and more enquiry-based lessons</li>

<p>I disseminated the course through a faculty meeting, where I highlighed the following as important in engaging any student in a subject they do not engage with.</p>
<ul>
<li>Use rich questioning to promote thinking skills</li>
<li>Control group work to ensure all students engage and take ownership of their role within the group</li>
<li>Do more open ended investigations</li>
<li>Use enrichment activities</li>
</ul>

<p> A "rich question" can be defined as "an open-ended, higher order question that requires learners to link or apply ideas, give reasons, summarise or evaluate. Sometimes the questions encourage learners to ask further questions to qualify what the question is actually asking them to explain. These questions generally require extended answers."

<h3>Outcomes</h3>
<p>To test its success I conducted a survey of my year 8 class to assess their confidence in subject, their enjoyment of it, and their ideas of how the topic links with the rest of KS3 Science. I found that before the study Rock was seen as a dull topic which students had not historically engaged well with. After rewriting this scheme of work, it is now a very popular module with students who look forward to the topic and are positive about it afterwards.</p>

<p>
Rich questioning on the whole was successful, leading to students generating their own curriculum links, improving group skills, and developing thinking skills. When posing a rich question I have discovered that it is very important to do it in random groups, with a nominated spokesperson/group leader and a specific task of producing a concept map (rather than "just discuss it"). Or if structured groups are not used, then it should be done individually but with discussion between neighbours allowed. Obviously group work will produce better discussion, but  is not always desirable in every lesson. The quality of the stimulus materials and the phrasing of the question are extremely important. It can be helpful to qualify a rich question with a couple of prompt questions, especially for lower attaining students. Providing the right pictures which help focus the discussion is good, while hands on stimulus material helps students show their understanding before they are able to fully explain their ideas in words.
                 	
