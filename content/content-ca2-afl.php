<h2>Assessment for Learning</h2>
<h3>About the project</h3>
<p>This was Curriculum Assignment 2 for the Oxford University PGCE course.</p>

<h3>What I did</h3>
<p>I started by reading "Inside the Black Box: v. 1: Raising Standards Through Classroom Assessment" by Dylan Williams & Paul Black. I then read "Working Inside the Black Box: Assessment for Learning in the Classroom" by Christine Harrison et. al. This gave me an early impression of what Assesment for Learning is and how it is useful in the classroom.</p>

<h3>Summary of findings</h3>
To me the term 'Assessment for Learning' is just a bye word for good teaching, but giving it this label helps teachers identify what we all should be doing. It is important for learners to see exactly where they are in their learning, and have feedback on how they can improve. Further assessment should then be able to identify this improvement.


<h3>Outcome</h3>
<p>in my own practice I use the techniques described in the above literature, including traffic light sheets, comment based marking with expectation on response from student, level descriptors..</p>

