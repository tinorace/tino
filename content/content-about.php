<h3>Education</h3>
<h4>School</h4>
I was born in Blackburn and raised in Langho, a village 4 miles to the north. I went to Heathfield (a now defunct primary school), follwed by 3 years at the junior school of <a href="http://www.qegs.blackburn.sch.uk/" target="_blank">Queen Elizabeth's Grammar School, Blackburn</a>. I then moved up to the secondary school at QEGS, where I focussed on scientific subjects, particularly Physics. I also enjoyed Latin and Design Technology and today continue to use the design and woodworking skills I learnt there.<br/>

<h4>University of Cambridge</h4>
I earned a place at the University of Cambridge to read Natural Sciences. I discovered a new subject which I found I loved: Geology. This became my specialism and will continue to be a life-long interest. <br/>

<h4>Oxford University & Teaching</h4>
After graduating with a Masters of Science (2.i) I moved to Oxford. Here I took a PGCE course at Oxford University (Physics), and whilst on placement I took a job at Lord Williams's School Thame, where I am currently a Physics teacher.<br/><br/>

