<h2>Bringing the Ocean to Oxfordshire, 2010-2011</h2>
<h3>About the project</h3>
<p>This two part course was for teachers who are experienced and confident in teaching secondary physics. I found out how my pupils could learn physics by engaging with research and applying it to my classroom practice. I tried a wide range of activities which I then used back in the classroom.</p>

<h3>Project aims</h3>
<ol>
<li>To develop and pilot a package of creative resources to support delivery of content and skills at key stage 3 through
the context of oceanography. Teaching resources will include paper-based, online and practical materials developed by
the group.</li>
<li>To increase pupils’ engagement and enjoyment of science at key stage 3 in the context of the new curriculum, and
therefore attainment.</li>
<li>To enable schools and colleges which do not have easy access to the ocean to use oceanography as a context to
teach content and skills at key stage 3.</li>
<li>To enable teachers, including non-specialists, to identify a range of opportunities in the key stage three curriculum
for teaching all three sciences in a context-based approach, including identification of opportunities to teach functional
skills including How Science Works and Personal Learning and Teaching Skills.</li>
<li>To develop a programme of CPD for teachers to support delivery of the package that can be disseminated in a wide
range of forums including a dissemination event, online (Science Learning Centre network, Oxfordshire County Council,
Astra Zeneca Science Teaching Trust) and via partner organisations (Science Learning Centre South East, Oxfordshire
County Council, Science Oxford, Oxford Museums).</li>
</ol>

<h3>Success Criteria</h3>
<ul>
<li>Increased teacher confidence and ability to identify opportunities for context-based learning in 
science at KS3 and to develop/select appropriate resources as measured by questionnaire and focus group 
or telephone interviews</li>
<li>Increased pupil engagement and enjoyment of science as measured by questionnaire</li>
<li>Increased attainment in relevant parts of KS3 as measured using teacher assessment</li>
<li>Increased understanding and use of oceanography as a context to teach content and skills at KS3</li>
<li>Take-up of resources and professional development opportunities by schools in Oxfordshire</li>
</ul>

<h3>What I did</h3>
<p>I created a six lesson scheme of work to teach a range of content and skill in the context of oceanography. There were some lessons to purely look at the importance of oceanography and try some of the practical skills used in this discipline. There was also one lesson looking at each of the Biology, Chemistry, and Physics aspects of oceanography. I planned the lessons and taught them to a year 8 group.</p>

